#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int				i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	if (n == 0)
		return (0);
	else
	{
		while (str1[i] && i < ((int)n - 1) && str1[i] == str2[i])
			i++;
	}
	return (str1[i] - str2[i]);
}
