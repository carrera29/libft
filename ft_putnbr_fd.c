#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		write (fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			write (fd, "-", 1);
			n *= -1;
		}
		if (n > 9)
		{
			ft_putnbr_fd(n / 10, fd);
			n = (n % 10);
		}
		if (n < 10 && n >= 0)
		{
			n = n + 48;
			write (fd, &n, 1);
		}
	}
}
